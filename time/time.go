package time

import "time"

const FormatYYYYMMDDHHMMSSNsZSNumHHMMSS = "2006-01-02 15:04:05.000000000 -07:00:00"
const FormatYYYYMMDDHHMMSSNsZZNumHHMMSS = "2006-01-02 15:04:05.000000000 Z07:00:00"
const FormatYYYYMMDDHHMMSSNsZSNumHHMM = "2006-01-02 15:04:05.000000000 -07:00"
const FormatYYYYMMDDHHMMSSNsZZNumHHMM = "2006-01-02 15:04:05.000000000 Z07:00"
const FormatYYYYMMDDHHMMSSNsZName = "2006-01-02 15:04:05.000000000 MST"

const FormatYYYYMMDDHHMMSSNsZNameZSNumHHMMSS = "2006-01-02 15:04:05.000000000 MST -07:00:00"
const FormatYYYYMMDDHHMMSSNsZNameZZNumHHMMSS = "2006-01-02 15:04:05.000000000 MST Z07:00:00"
const FormatYYYYMMDDHHMMSSNsZNameZSNumHHMM = "2006-01-02 15:04:05.000000000 MST -07:00"
const FormatYYYYMMDDHHMMSSNsZNameZZNumHHMM = "2006-01-02 15:04:05.000000000 MST Z07:00"

const FormatDefault = FormatYYYYMMDDHHMMSSNsZNameZSNumHHMMSS

func LocalFormatDefault(t time.Time) string {
	return t.Local().Format(FormatDefault)
}
